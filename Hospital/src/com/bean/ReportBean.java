package com.bean;
public class ReportBean {

    private int Report_ID ;
    private String Report_PatientName ;
    private String Report_Disease ;
    private String Report_Medicine ;
	public int getReport_ID() {
		return Report_ID;
	}
	public void setReport_ID(int ReportID) {
		Report_ID = ReportID;
	}
	public String getReport_PatientName() {
		return Report_PatientName;
	}
	public void setReport_PatientName(String ReportPatientName) {
		Report_PatientName = ReportPatientName;
	}
	public String getReport_Disease() {
		return Report_Disease;
	}
	public void setReport_Disease(String ReportDisease) {
		Report_Disease = ReportDisease;
	}
	public String getReport_Medicine() {
		return Report_Medicine;
	}
	public void setReport_Medicine(String ReportMedicine) {
		Report_Medicine = ReportMedicine;
	}
}
