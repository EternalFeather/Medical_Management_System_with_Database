package com.bean;
public class HospitalBean {

    private int Hospital_ID ;
    private String Hospital_Name ;
    private String Hospital_Address ;
    private String Hospital_Scale ;
	public int getHospital_ID() {
		return Hospital_ID;
	}
	public void setHospital_ID(int HospitalID) {
		Hospital_ID = HospitalID;
	}
	public String getHospital_Name() {
		return Hospital_Name;
	}
	public void setHospital_Name(String HospitalName) {
		Hospital_Name = HospitalName;
	}
	public String getHospital_Address() {
		return Hospital_Address;
	}
	public void setHospital_Address(String HospitalAddress) {
		Hospital_Address = HospitalAddress;
	}
	public String getHospital_Scale() {
		return Hospital_Scale;
	}
	public void setHospital_Scale(String HospitalScale) {
		Hospital_Scale = HospitalScale;
	}
}
