package com.bean;
public class PatientBean {

    private int Patient_ID ;
    private String Patient_Name ;
    private int Patient_Age ;
    private String Patient_Disease ;
	public int getPatient_ID() {
		return Patient_ID;
	}
	public void setPatient_ID(int PatientID) {
		Patient_ID = PatientID;
	}
	public String getPatient_Name() {
		return Patient_Name;
	}
	public void setPatient_Name(String PatientName) {
		Patient_Name = PatientName;
	}
	public int getPatient_Age() {
		return Patient_Age;
	}
	public void setPatient_Age(int PatientAge) {
		Patient_Age = PatientAge;
	}
	public String getPatient_Disease() {
		return Patient_Disease;
	}
	public void setPatient_Disease(String PatientDisease) {
		Patient_Disease = PatientDisease;
	}
}
